Route::get('/', function () {
    return view('gigs');
});

Route::post('/gig', function (Request $request) {
    $validator = Validator::make($request->all(), [
        'name' => 'required|max:255',
    ]);

    if ($validator->fails()) {
        return redirect('/')
            ->withInput()
            ->withErrors($validator);
    }

    $gig = new Gig;
    $gig->name = $request->name;
    $gig->save();

    return redirect('/');
});